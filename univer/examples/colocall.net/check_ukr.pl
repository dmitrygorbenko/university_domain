sub ukr_ip {
my $uaddr=shift;
map {
  my ($oaddr,$mask)=split/\//,$_;
  my $poaddr=unpack("L",pack("C4",(split (/\./, $oaddr))));
  my $puaddr=unpack("L",pack("C4",(split (/\./, $uaddr))));
  my $pmask=unpack("L",pack("B32",(1 x $mask)));
  return 1 if (($puaddr & $pmask)==($poaddr & $pmask));
 }(<DATA>);
return 0;
}


